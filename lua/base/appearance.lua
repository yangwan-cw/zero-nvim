-- 外观、主题和视觉效果设置

local opt = vim.opt
local o = vim.o
local wo = vim.wo

-- ============================================
-- 行号显示
-- ============================================
wo.number = true                  -- 显示绝对行号
wo.relativenumber = true          -- 显示相对行号（当前行显示绝对行号，其他行显示相对距离）
                                  -- 相对行号便于使用 5j, 10k 等命令快速跳转

-- ============================================
-- 光标和当前行高亮
-- ============================================
wo.cursorline = true              -- 高亮当前光标所在行（便于定位当前位置）

-- ============================================
-- 侧边栏和参考线
-- ============================================
wo.signcolumn = "yes"             -- 始终显示左侧标志列（用于显示 git 状态、诊断信息等）
                                  -- "yes" 固定显示，避免出现/消失导致的文本跳动
wo.colorcolumn = "80"             -- 在第80列显示竖线（代码长度参考线）
                                  -- 提醒代码行不要太长，保持可读性

-- ============================================
-- 折行设置
-- ============================================
wo.wrap = false                   -- 禁止自动折行（长行会超出屏幕，需要水平滚动）
                                  -- 适合代码编辑，保持代码结构清晰

-- ============================================
-- 不可见字符显示
-- ============================================
o.list = true                     -- 显示不可见字符（空格、Tab等）
o.listchars = "space:·"           -- 将空格显示为中点 · （便于识别缩进和对齐）
                                  -- 可以添加更多：tab:→\ ,eol:↴,trail:~

-- ============================================
-- 颜色和主题
-- ============================================
o.termguicolors = true            -- 启用24位真彩色支持（让主题颜色更丰富准确）
opt.termguicolors = true          -- 两种写法都设置，确保生效
o.background = "dark"             -- 设置背景为深色（影响某些主题的颜色方案）

-- ============================================
-- 状态栏和标签栏
-- ============================================
o.showtabline = 2                 -- 始终显示标签栏
                                  -- 0: 永不显示, 1: 多于一个标签时显示, 2: 总是显示
o.showmode = false                -- 不显示当前模式（-- INSERT -- 等）
                                  -- 使用状态栏插件后这个信息是多余的
